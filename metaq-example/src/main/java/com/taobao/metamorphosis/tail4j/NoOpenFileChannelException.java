package com.taobao.metamorphosis.tail4j;

public class NoOpenFileChannelException extends RuntimeException{

    public NoOpenFileChannelException() {
        super();
      
    }

    public NoOpenFileChannelException(String message, Throwable cause) {
        super(message, cause);
      
    }

    public NoOpenFileChannelException(String message) {
        super(message);
      
    }

    public NoOpenFileChannelException(Throwable cause) {
        super(cause);
      
    }

}
